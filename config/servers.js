/**
 * Obtener configuración de servidor por ID
 */
function getServerById(serverId) {
  return serversConfig.find(s => s.id === serverId);
}

/**
 * Obtener todos los servidores
 */
function getAllServers() {
  return serversConfig.map(({ username, password, ...rest }) => rest);
}

/**
 * Obtener información de servidor (sin credenciales)
 */
function getServerInfo(serverId) {
  const server = getServerById(serverId);
  if (!server) return null;
  
  const { password, ...info } = server;
  return info;
}

